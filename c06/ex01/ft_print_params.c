#include <unistd.h>
int	main(int	ac, char	**av)
{
	int	i;
	int	j;

	j = 1;
	if (ac > 1)
	{
		while (av[j])
		{
			i = 0;
			while (av[j][i])
			{
				write(1, &av[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j++;
		}
	}
	return (0);
}
