int	ft_str_is_lowercase(char	*str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str [i] != '\0')
	{
		if (str[i] <= 'z' && str[i] >= 'a')
			j = 1;
		else
		{
			j = 0;
			break ;
		}
		i++;
	}
	return (j);
}
